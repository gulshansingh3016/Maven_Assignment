public class Calculator {
    public static void addition(final float number1, final float number2) {
        System.out.println("Addition is " + (number1 + number2));
    }
    public static void subtraction(final float number1, final float number2) {
        System.out.println("Subtraction is " + (number1 - number2));
    }
    public static void division(final float number1, final float number2) {
        System.out.println("Division is " + (number1 / number2));
    }
    public static void multiply(final float number1, final float number2) {
        System.out.println("Multiplication is " + (number1 * number2));
    }
}
